// Operator <
console.log(`2 < 4 = `, 2 < 4 );

console.log('---------------');

// Operator >
console.log(`2 > 4 = `, 2 > 4);

console.log('---------------');

// Operator <=
console.log(`2 <= 4 = `, 2 <= 4);

console.log('---------------');

// Operator >=
console.log(`2 >= 4 = `, 2 >= 4);

console.log('---------------');

// Operator Combine
console.log(`2 < 4 < 2 == 2 =`, 2 < 4 < 2 == 2);

// Operator sama dengan
console.log(`"1" == 1 =>`, `1` == 1);

// Operator sama dengan tipe
console.log(`"1" === 1 =>`, `1` === 1);

// Operator tidak sama dengan
console.log(`"1" == 1 =>`, `1` != 1);

// Operator tidak sama dengan tipe
console.log(`"1" === 1 =>`, `1` !== 1);